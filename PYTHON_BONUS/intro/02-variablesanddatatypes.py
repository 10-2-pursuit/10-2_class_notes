# A variable is a name that refers to a value.
# Here we are creating variables of different data types.

# Integer (int)
age = 25

# Floating-point number (float)
height = 5.9

# String (str)
name = "Alice"

# Boolean (bool)
is_student = True


# Print the values of the variables
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Is Student:", is_student)
